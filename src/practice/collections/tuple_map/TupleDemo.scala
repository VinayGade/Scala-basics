package practice.collections.tuple_map

object TupleDemo extends App {

  val fePair1 = (1, "FE")
  val fePair2 = (2, "INST")
  val fePair3 = (3, "EXTC")
  val fePair4 = (4, "ELTR")
  val fePair5 = (5, "CM")
  val fePair6 = (6, "IT")
  println(fePair1._1+" "+fePair1._2)
  println(fePair2._1+" "+fePair2._2)
  println(fePair3._1+" "+fePair3._2)
  println(fePair4._1+" "+fePair4._2)
  println(fePair5._1+" "+fePair5._2)
  println(fePair6._1+" "+fePair6._2)
}
